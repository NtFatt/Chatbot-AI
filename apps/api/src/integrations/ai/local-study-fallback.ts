const detectList = (input: string) =>
  input
    .toLowerCase()
    .split(/[^a-z0-9\u00c0-\u1ef9]+/i)
    .filter((item) => item.length >= 3)
    .slice(0, 6);

const normalizeTopicLabel = (input: string) =>
  input
    .trim()
    .replace(/[?！？]+$/g, '')
    .replace(/\s+là gì$/i, '')
    .replace(/\s+la gi$/i, '')
    .trim();

const buildRelatedTerms = (question: string, keywords: string[]) => {
  const topicLabel = normalizeTopicLabel(question).toLowerCase();
  const topicTokens = new Set(topicLabel.split(/\s+/).filter(Boolean));

  const filtered = keywords.filter((keyword) => !topicTokens.has(keyword.toLowerCase()));
  return filtered.slice(0, 4);
};

